pub fn kernel_init(k_type: Kernel) -> (Vec<Vec<Vec<f64>>>, Vec<f64>){
    match k_type{
        Kernel::Ring1(h) => {
            return ring_kernel1(h)
        },
        Kernel::Ring2(h1, h2) => {
            return ring_kernel2(h1, h2)
        },
        Kernel::Bumpy(p) => {
            return bumpy_kernel(&p);
        },
        Kernel::Radical(p) => {
            return radical_kernel(&p);
        }
    }
}


