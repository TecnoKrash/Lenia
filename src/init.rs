fn bumpy_kernel(p: &Param) -> (Vec<Vec<Vec<f64>>>, Vec<f64>){
    println!("p : {:?}", p);
    let h = 2*p.gr +1;
    let mut result = vec![vec![vec![0.0 ; h]; h]; p.nb_kernels];


    let mut sum = vec![0.0; p.nb_kernels];
    
    for k in 0..p.nb_kernels {
        for x in 0..h {
            for y in 0..h {
                let dx;
                let dy;
                if x > p.gr { dx =  x-p.gr}
                else { dx = p.gr-x}
                if y > p.gr { dy =  y-p.gr}
                else { dy = p.gr-y}

                let distance = ((dx*dx + dy*dy) as f64).sqrt()/(p.r[k]*(p.gr as f64));
                // println!("{}", distance);
                if distance <= 1.0 {
                    for i in 0..p.nb_bump[k]{
                        let d_gauss = p.b[k][i]*gaussian(p.a[k][i],p.w[k][i],distance);
                        // println!("{}", d_gauss);
                        sum[k] += d_gauss;
                        result[k][x][y] += d_gauss;
                    }
                }
            }
        }
        // println!("k: {}, gr: {}, r: {}\na: {:?}\nw: {:?}\nb: {:?}\n", k, gr, r, a, w, b);



        for i in 0..h{
            for j in 0..h{
                result[k][i][j] /= sum[k];
            }
        }
    }


    (result,sum)
}

fn radical_kernel(p: &Param) -> (Vec<Vec<Vec<f64>>>, Vec<f64>){
    println!("p : {:?}", p);
    let h = 2*p.gr +1;
    let mut result = vec![vec![vec![0.0 ; h]; h]; p.nb_kernels];

    let mut sum = vec![0.0; p.nb_kernels];
    
    for k in 0..p.nb_kernels {
        for x in 0..h {
            for y in 0..h {
                let dx;
                let dy;
                if x > p.gr { dx =  x-p.gr}
                else { dx = p.gr-x}
                if y > p.gr { dy =  y-p.gr}
                else { dy = p.gr-y}

                let distance = ((dx*dx + dy*dy) as f64).sqrt()/(p.r[k]*(p.gr as f64));
                // println!("{}", distance);
                if distance <= 1.0 {
                    for i in 0..p.nb_bump[k]{
                        let mut d_gauss = p.b[k][i]*gaussian(p.a[k][i],p.w[k][i],distance);
                        // println!("{}", d_gauss);
                        if 1.0 - d_gauss > d_gauss { d_gauss = 0.0; }
                        else { d_gauss = 1.0; }
                        sum[k] += d_gauss;
                        result[k][x][y] += d_gauss;
                    }
                }
            }
        }
        // println!("k: {}, gr: {}, r: {}\na: {:?}\nw: {:?}\nb: {:?}\n", k, gr, r, a, w, b);


        for i in 0..h{
            for j in 0..h{
                result[k][i][j] /= sum[k];
            }
        }
    }


    (result, sum)
}

