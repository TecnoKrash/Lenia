pub struct Field {
    pub t: f64,
    pub l: usize,
    pub h: usize,
    pub k_size: usize,
    pub nb_channels: usize,
    pub m: Vec<Vec<Vec<f64>>>,
}

pub enum Kernel<'a> {
    Ring1(usize),
    Ring2(usize, usize),
    Bumpy(& 'a Param),
    Radical(& 'a Param),
}

#[derive(Clone)]
pub enum Motif {
    Agent(Agent),
    Rand(usize, usize),
}

#[derive(Clone)]
pub enum Agent {
    Orbium,
    Hydrogeminium,
}

impl Field {
    // A function to create an empty field
    pub fn new_field(h: usize, l: usize, nb_channels: usize) -> Field {
        Field {
            t: 0.0,
            l,
            h,
            k_size: 0,
            nb_channels: nb_channels,
            m: vec![vec![vec![0.; l]; h]; nb_channels],
        }
    }

    pub fn to_tore(mut self: Field, kernel: Vec<Vec<f64>>){
        for i in 0..self.nb_channels{
            self.m[i] = tore_format(&self.m[i], &kernel);
        }
        self.k_size = kernel.len();
    }

    pub fn get_xy(self: &Field, x: usize, y: usize, chanel: usize) -> f64{
        self.m[chanel][x][y]
    }

    pub fn fill(self: &mut Field,chan: usize, val: f64){
        for i in 0..self.h{
            for j in 0..self.l{
                self.m[chan][i][j] = val;
            }
        }
    }

    pub fn fill_deg(self: &mut Field, chan: usize, start: f64, end: f64){
        for i in 0..self.h{
            for j in 0..self.l{
                let fi = i as f64;
                let fj = j as f64;
                let fl = self.l as f64;
                let fh = self.h as f64;
                self.m[chan][i][j] = start + (fi+fj)*(end)/(fh+fl);
            }
        }
    }

    pub fn fill_rng(self:&mut Field, chan: usize){
        let mut rng = rand::thread_rng();
        for i in 0..self.h{
            for j in 0..self.l{
                self.m[chan][i][j] = rng.gen::<f64>();
            }
        }
    }

    pub fn add(self:&mut Field, set: &Settings, x: usize, y: usize){
        let m;
        match &set.motif{
            Motif::Agent(agent) => {
                match agent {
                    Agent::Orbium => {
                        m = read_field("storage/creature/orbium.txt")
                    },
                    Agent::Hydrogeminium => {
                        m = hydrogeminium();
                    },
                }

            },
            Motif::Rand(h, l) => {
                println!("huh");
                m = random_square(*h, *l, set.mode.clone());
            }
        }
        
        if (y + m[0].len() < self.l) && (x + m.len() < self.h){
            for i in 0..m.len(){
                for j in 0..m[0].len(){
                    self.m[0][x+i][y+j] = m[i][j];
                }
            }
        }
    }

}



